def build_resume_markdown() -> str:
    lines: list[str] = [
        f"# {NAME}",
        ROLE,
        "",
        f"- Email: {EMAIL}",
        f"- Phone: {PHONE}",
        f"- GitHub: {GITHUB_URL}",
        f"- LinkedIn: {LINKEDIN_URL}",
        "",
        "## Summary",
        SUMMARY,
        "",
        "## Education",
    ]

    for item in EDUCATION:
        lines.extend(
            [
                f"### {item['school']}",
                f"**{item['degree']}**  \n{item['duration']}",
                "",
            ]
        )

    lines.append("## Technical Skills")
    for category, values in SKILLS.items():
        lines.append(f"**{category}:** {', '.join(values)}")
    lines.append("")

    lines.append("## Projects")
    for project in FEATURED_PROJECTS:
        lines.extend(
            [
                f"### {project['title']}",
                f"**Stack:** {project['stack']}  \n**Link:** {project['link']}  \n**GitHub:** {project['github']}",
            ]
        )
        for bullet in project["bullets"]:
            lines.append(f"- {bullet}")
        lines.append("")

    lines.append("## Achievements & Extracurriculars")
    for achievement in ACHIEVEMENTS:
        lines.append(f"- {achievement}")
    lines.append("")

    lines.append("## Certifications")
    for cert in CERTIFICATIONS:
        lines.append(f"- **{cert['name']}** — {cert['issuer']} ({cert['date']})")
    lines.append("")

    return "\n".join(lines)
